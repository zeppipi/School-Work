package game.enums;

/**
 * Enum that represents a passive skill for weapons
 */
public enum Passives {
    CRITICAL_STRIKE, // Have 20% rate to deal double damage
    DULLNESS,       // Deal half damage to non-Lord of Cinder
    RAGE_MODE,        // Increase hit rate by 30%
    RANGED       // Can attack enemy from 3 squares away
}
